//Math methods in javascript.

//ceil()
//this will print the nearest upper value of the float value.

var a = Math.ceil(3.2);
document.write(a);
document.write("<br><br>");

//floor()
//This will produce the nearest downword value.

var b = Math.floor(4.5);
document.write(b);
document.write("<br><br>");

//round()
//this also displays the nearest integer value like if it's .5> the upword value else downword value.

var c = Math.round(3.4);
document.write(c);
document.write("<br><br>");

//trunc()
//this will return the simple integer value.

var d = Math.trunc(12.4);
document.write(d);
document.write("<br><br>");

//max() & min()
//max will return the maximum value and min will return value

var e = Math.max(12,14,45,89,90);
document.write(e);
document.write("<br><br>");
var f = Math.min(12,8,4,50,34);
document.write(f);
document.write("<br><br>");

//sqrt() & cbrt()
//square root and cube root

var g = Math.sqrt(64);
var h = Math.cbrt(27);
document.write(g);
document.write("<br><br>");
document.write(h);
document.write("<br><br>");

//pow()
//produce the power of a value

var i = Math.pow(5,3);
document.write(i);
document.write("<br><br>");

//random()
//Produces a random value in between given values.

var j = Math.random(10,20);
document.write(j);
document.write("<br><br>");

//abs() = absolute value
//produces the absolute value 

var k = Math.abs(-12.5);
document.write(k);
document.write("<br><br>");

//PI ind js

var l = Math.PI;
document.write(l);


